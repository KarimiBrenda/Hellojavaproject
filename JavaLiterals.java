public class JavaLiterals {
    public static void main (String[] args){
        /**
         * Java Literals
         * There are different types Literals
         * i. Integer Literals
         * ii. Floating point literals
         * iii. Boolean literals
         * iv. Character literals
         * v. String literals
         */

        //1. Integer literals
        // They come in different formats
        // -> decimal (base 10)
        // it doesn't want special notation e.g 12)
        System.out.println("Example of a decimal literal is:  "+ 12);

        // --> hexadecimal (base 16)
        // preceded by 0X E.G 0XC
        System.out.println("Example of a Hexadecimal literal is: "+ 0Xf);
        System.out.println("Example of a octal literal is: "+ 0x15);

        // --> octal (base 8)
        // preceded by 0 e.g 014

        // 2. floating point literals
        //Represents decimals with fractional poarts e.g 3.1416
        // can be expressed in standard or scientific notation
        //e.g 583.45 (standard) or 5.8345e2
        System.out.println("Example of a standard float literal is "+583.45);

        //3. Boolean literals
        //They have only two values, true or false
        System.out.println("Example of Boolean literal is: true or false");

        //4. Character literals
        //represents single unicode characters
        // A Unicode character set replaces the 8-bit ASCII set
        //Unicode allows the inclusion of symbols and special characters
        //From other languages
        //To use a character literal, encloses the character in single quote.
         //letter 'a' is represented as 'a'
        System.out.println('a');

        System.out.println( "This is a paragraph with \n a new line or");

                //5. String literals
                //represents multiple characters and are enclosed by double quote
                //example; 'Hello world!"
                System.out.println("Hello world!");

        // Tomorrow -- primitive Data types
        //




    }
    }

