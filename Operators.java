public class Operators {
    /**
     * Operators in java
     * We have different types of operators in java
     * 1. Arithmetic operators
     * 2. Relation operators
     * 3.Logical operators
     * Conditional operators
     *
     * They follow a certain precedence
     */
    public static void main(String[] args){
        //create two ineger
        int x =20;
        int y =6;
        //1. Arithmetic operators
        // Addition ->
        System.out.println(x+y); // returns 26

        //Subtraction ->
        System.out.println(x-y); // returns 14

        //multiplication ->
        System.out.println(x*y); // returns 12o

        //Division
        System.out.println(x/y) ;// returns 3.33333333
        //Modulus ->(finds the reminder ofa division)
        System.out.println(x%y); //returns 2
    }
}
